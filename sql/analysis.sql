-- 1️. Customer Insights
-- Gain an understanding of customer engagement and purchasing behavior.
-- ● How many unique customers have placed orders?
SELECT COUNT(DISTINCT customerid) as unique_customer from orders;

-- Which customers have placed the highest number of orders?
select customerid,count(*) as cust_highest_order from orders group by customerid order by cust_highest_order desc limit 1;

-- ● What is the total and average purchase value per customer?
SELECT customerid,
sum(od.totalprice) as total_purchase,
avg(od.totalprice) as avg_purchase
from orders o
JOIN orderdetails od 
ON o.orderid  = od.orderid
GROUP BY o.customerid;


-- ● Who are the top 5 customers by total purchase amount?
SELECT customerid,
sum(od.totalprice) as total_purchase
from orders o
JOIN orderdetails as od 
on o.orderid = od.orderid
group by customerid
order by total_purchase desc limit 5;


