 -- ● On which dates were the most orders placed?
 select orderdate, count(*) as total_order  from orders group by orderdate order by total_order desc limit 1;
 
 -- ● What are the monthly trends in order volume and revenue?
 select 
month(o.orderdate) as month,
 count(distinct o.orderid) as total_orders,
 sum(od.totalprice) as total_revenue
 from orders o
join orderdetails od
on o.orderid = od.orderid
 group by month(o.orderdate);
 
 
 -- ● How do order patterns vary across weekdays and weekends
 SELECT 
    CASE 
        WHEN DAYOFWEEK(orderdate) IN (1,7) THEN 'Weekend'
        ELSE 'Weekday'
    END AS day_type,
    COUNT(*) AS total_orders
FROM orders
GROUP BY day_type;

-- 4️. Supplier Contribution
-- Identify the most active and profitable suppliers.
select*from suppliers;
select* from products;
-- ● How many suppliers are there in the database?
select count(*) as total_suppliers from suppliers;

-- Which supplier provides the most products?
select s.suppliername,
count(p.productid) as total_products
from suppliers s 
join products p 
 on s.supplierid  = p.productid
 group by suppliername
 order by total_products desc limit 1;

-- ● What is the average price of products from each supplier?
SELECT 
    s.suppliername,
    AVG(p.price) AS avg_price
FROM suppliers s
JOIN products p 
    ON s.supplierid = p.supplierid
GROUP BY s.suppliername;

-- ● Which suppliers contribute the most to total product sales (by revenue)?
SELECT 
    s.supplierid,s.suppliername,
    SUM(od.totalprice) AS total_revenue
FROM orderdetails od
JOIN products p 
    ON od.productid = p.productid
JOIN suppliers s 
    ON p.supplierid = s.supplierid
GROUP BY s.supplierid,suppliername
ORDER BY total_revenue DESC;


-- 5️. Employee Performance
-- Assess how employees are handling and influencing sales.
select* from employees;
select*from orders;
-- ● How many employees have processed orders?
select count(distinct employeeid) as total_orders from orders;

-- ● Which employees have handled the most orders?
SELECT 
    e.employeeid,
    e.name,
    COUNT(o.orderid) AS total_orders
FROM employees e
JOIN orders o 
    ON e.employeeid = o.employeeid
GROUP BY e.employeeid, e.name
ORDER BY total_orders DESC;

-- ● What is the total sales value processed by each employee?
-- Total sales per employee
select 
e.employeeid,
e.name,
sum(od.totalprice) as total_sales
from employees e
join orders o
on e.employeeid = o.employeeid
join orderdetails od
on o.orderid = od.orderid
group by e.employeeid,e.name;


-- ● What is the average order value handled per employee?
-- Average order value per employee
SELECT 
    e.employeeid,
    e.name,
    AVG(order_total) AS avg_order_value
FROM (
    SELECT 
        o.employeeid,
        o.orderid,
        SUM(od.totalprice) AS order_total
    FROM orders o
    JOIN orderdetails od 
        ON o.orderid = od.orderid
    GROUP BY o.employeeid, o.orderid
) AS temp
JOIN employees e 
    ON temp.employeeid = e.employeeid
GROUP BY e.employeeid, e.name;


